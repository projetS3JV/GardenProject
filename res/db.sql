CREATE TABLE plante (
	id 			INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0, INCREMENT BY 1) PRIMARY KEY,
	nom_Usuel		VARCHAR(25),
	nom_Latin 		VARCHAR(25),
	type_Plante		INTEGER,
	hauteur			INTEGER,
	luminosite		INTEGER,
	url_Image_En_Fleur	VARCHAR(100),
	couleur_en_fleur	INTEGER,
	couleur_non_fleuris	INTEGER,
	type_Sol		INTEGER ARRAY,
	date_Plantation		DATE,
	date_DebutFloraison	DATE ARRAY,
	date_FinFloraison	DATE ARRAY,
	vivace			BOOLEAN,
	description		VARCHAR(1000)
)

CREATE TABLE jardin (
	id 			INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0, INCREMENT BY 1) PRIMARY KEY,
	nom			VARCHAR(25),
	width			INTEGER,
	height			INTEGER
)

CREATE TABLE zone (
	id 			INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0, INCREMENT BY 1) PRIMARY KEY,
	id_Jardin		INTEGER FOREIGN KEY REFERENCES jardin(id),
	x			INTEGER ARRAY,
	y			INTEGER ARRAY,
	luminosite		INTEGER
)

CREATE TABLE zonePlantable (
	id 			INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0, INCREMENT BY 1) PRIMARY KEY,
	id_Plante		INTEGER FOREIGN KEY REFERENCES plante(id),
	id_Zone			INTEGER FOREIGN KEY REFERENCES zone(id),
	x			INTEGER ARRAY,
	y			INTEGER ARRAY,
	type_Sol		INTEGER,
	luminosite		INTEGER
)












